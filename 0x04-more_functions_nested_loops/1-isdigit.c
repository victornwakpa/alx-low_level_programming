#include <stdio.h>
#include "main.h"
/**
 * _isdigit - the main function for this program
 * @c: hello
 * Description: A function that checks if c is a digit
 * Return: This returns 1 if c is a digit and 0 if otherwise
 */

int _isdigit(int c)
{
	if (c >= '0' && c <= '0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
